import API from "./api";
import {baseURL} from './urls';

export const register = async (param) => {
  try{
    return await API.post(`${baseURL}/register`, param).then( 
      response => {
        return response.data;
      },
      error =>{
        console.log(error);
        return  null;
      }
    );
  }catch(error){
    console.log(error);
    return null;
  }
}

